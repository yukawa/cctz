// MakeTime tests for America/New_York:
//   Spring-forward gap:  02:30 local on Mar 10, 2024 does not exist (SKIPPED)
//   Fall-back overlap:   01:30 local on Nov  3, 2024 is ambiguous (REPEATED)
TEST(CreateWinZoneInfoSource, MakeTime_America_NewYork) {
  const auto source = CreateWinZoneInfoSource(kEasternStandardTime);
  ASSERT_TRUE(!!source);
  auto tz = TimeZoneInfo::MakeFromSourceForTesting(source.get());
  ASSERT_TRUE(!!tz);

  // Skipped time: 02:30 EST gap.
  // pre  uses pre-transition offset EST(-18000): 02:30 + 5h = 07:30 UTC
  // post uses post-transition offset EDT(-14400): 02:30 + 4h = 06:30 UTC
  // trans = 07:00 UTC (the spring-forward instant)
  ExpectMakeTime(tz, 2024, 3, 10, 2, 30, 0, time_zone::civil_lookup::SKIPPED,
                 FromUTC(2024, 3, 10, 7, 30, 0), FromUTC(2024, 3, 10, 7, 0, 0),
                 FromUTC(2024, 3, 10, 6, 30, 0));

  // Repeated time: 01:30 EDT/EST overlap.
  // pre  uses pre-transition offset EDT(-14400): 01:30 + 4h = 05:30 UTC
  // post uses post-transition offset EST(-18000): 01:30 + 5h = 06:30 UTC
  // trans = 06:00 UTC (the fall-back instant)
  ExpectMakeTime(tz, 2024, 11, 3, 1, 30, 0, time_zone::civil_lookup::REPEATED,
                 FromUTC(2024, 11, 3, 5, 30, 0), FromUTC(2024, 11, 3, 6, 0, 0),
                 FromUTC(2024, 11, 3, 6, 30, 0));
}

