time_point<seconds> FromUTC(year_t year, diff_t month, diff_t day, diff_t hour,
                            diff_t min, diff_t sec) {
  return FromUnixSeconds(civil_second(year, month, day, hour, min, sec) -
                         civil_second(1970, 1, 1, 0, 00, 00));
}

